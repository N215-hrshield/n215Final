export function modelPageName(pgName){
    $("#app").html(eval(pgName));
    if (pgName == "homeContent"){
        $("nav a").removeClass("blacktext").addClass("whitetext")
        $("nav section").removeClass("logoblack").addClass("logowhite")
        $("nav").removeClass("navblack").addClass("navwhite")
    }
    else {
        $("nav a").removeClass("whitetext").addClass("blacktext")
        $("nav section").removeClass("logowhite").addClass("logoblack")
        $("nav").removeClass("navwhite").addClass("navblack")
    }
}